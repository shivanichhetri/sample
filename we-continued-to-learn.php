                  <h1 class="">Tracking rhinos</h1>     
                  <p>Two rhinos – a sub-adult and adult female – were successfully collared in Khata Corridor on 27 November 2014 and 10 March 2015 respectively. Both the rhinos were fitted with a satellite-GPS collar, data from which will be key to providing conservationists with insights on habitat use and movement patterns of the rhinos in the trans-boundary corridor that links protected areas of Nepal and India and also help assess the thresholds of habitat restoration necessary to maintain a functional corridor for rhinos and other wildlife. Based on the telemetric data received from the collared rhinos, the home range of the sub-adult female and adult female rhinos was found to span over Nepal and India, covering the floodplains of Khata Corridor, Bardia National Park and Katerniaghat Wildlife Sanctuary (India). </p>    
