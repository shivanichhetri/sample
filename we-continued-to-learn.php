                  <h1 class="">THE HILL THAT BREATHES</h1>      
                  <p>Someshwor hill forest is a trans-boundary corridor that connects Nepal’s Chitwan National Park with India’s Valmiki Tiger Reserve. An ecological monitoring study undertaken in the area in April 2015 helped unearth valuable information on the status of wildlife, primarily tigers, in the area making it an important site in TAL for future conservation programming. Based on a camera trapping methodology covering a total area of 244 sq. km, the study identified 11 individual tigers and the presence of all three co-predators, the common leopard, sloth bear and Asiatic wild dog. Of the 26 mammalian species identified overall, twelve were of high global conservation significance categorized as Endangered, Vulnerable and Near Threatened under ‘IUCN’s Red List of Threatened Species’. </p>      
