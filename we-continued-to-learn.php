                  <h1 class="">Real-time SMART patrolling</h1>      
                  <p>Nepal pioneered the use of Real-time SMART Patrols in 83 guard posts in all tiger-bearing protected areas of TAL except Parsa Wildlife Reserve. This new patrol technique developed by the Nepal Army in collaboration with WWF Nepal makes use of an android-based platform on mobile devices through which patrol teams record and update patrolling and locational data in real-time. The most productive outcomes of the implementation of Real-time SMART Patrol are that it has allowed for 24- hour monitoring of patrol teams together with providing immediate instructions on the ground, made the patrolling teams accountable, increased area coverage and frequency, and provided for paperless and prompt reporting to the headquarters. Real-time SMART Patrols are the latest addition in the use of technology to curb poaching and aid enforcement efforts within protected areas.</p>     
