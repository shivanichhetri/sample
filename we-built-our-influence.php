                  <h1 class="">Strengthening national policy</h1>
                  <p>WWF Nepal is working with the Ministry of Land Reform and Management to strengthen the National Land Use Policy incorporating the elements of hazards on land use planning. Critical engagements have also been initiated with stakeholders such as Nepal Electricity Authority, Department of Roads, Department of Railways, National Hydropower Association and the private sector on sustainable infrastructure development. As a key output, the Department of Railways is already examining the possibility of alternative alignments to change the proposed east-west railway that was planned through Chitwan National Park. The proposed railway cuts through prime wildlife habitat in Nepal’s oldest national park which is home to the highest population of tigers and rhinos.</p> 
                  <p>Following the earthquake on April 25th, WWF Nepal supported the Ministry of Science, Technology and Environment to conduct a Rapid Environmental Assessment (REA) of the immediate and post-impacts of the earthquake to the environment. A key outcome of the REA is a strategy for minimizing the impacts of recovery and reconstruction in order to build back better and greener and in a more environmentally sustainable manner across mulitple sectors.</p>
