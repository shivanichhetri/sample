                  <p>A total of 50,000 youth signed up to WWF Nepal’s The Generation Green (TGG) campaign. The campaign, which seeks to build youth voice and action for the environment and Nepal’s sustainable development, has a target of creating 500,000 youth members within 2018. As one of the flagship projects of the campaign, a unique mentorship program was initiated bringing together 11 leading Nepali citizens from different sectors to mentor select TGG members. Under the mentorship program, TGG members conceptualized five projects focusing on environment sustainability, conservation education and waste management. Over the course of the campaign, youth will be provided opportunities to engage in various learning platforms, green projects and volunteering assignments with the end motive of building Nepal’s future leaders.</p>
