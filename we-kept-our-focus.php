          <h1 class="">Conservation with benefits</h1>      
          <p>Degradation of forests through slash-and-burn agriculture by poor families with no alternative is common in parts of Nepal. This practice disrupts biological corridors and causes soil erosion and landslides. Planting broom grass was the answer to rehabilitating degraded areas and improving livelihoods of local communities in Tanahun district, supported by the USAID-funded Hariyo Ban Program. From 2012 to 2014, local communities harvested approximately 110 metric tons of broom grass from 108 leasehold forests covering 226ha. Average annual household income increased by USD 66.25 for 782 poor families. At the same time soil erosion and stream sediment decreased and the forest started recovering, helping to restore the northsouth corridor linking Chitwan National Park with Annapurna Conservation Area.</p>
          <p>The Hariyo Ban Program is a five-year, USAID-funded program that aims to reduce the adverse impacts of climate change and threats to biodiversity in Nepal. It has three cross-cutting themes: livelihoods, governance, and gender and social inclusion. The program is being implemented by a consortium led by WWF in partnership with Cooperative for Assistance and Relief Everywhere (CARE), National Trust for Nature Conservation (NTNC), and the Federation of Community Forestry Users in Nepal (FECOFUN).</p>    
