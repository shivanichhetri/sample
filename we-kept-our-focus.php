                  <h1 class="">Towards resilient communities</h1>
                  <p>A total of 1,200 households in TAL, SHL and CHAL benefitted from the implementation of community and local adaptation plans of action. These adaptation plans are key guiding documents that enable communities strengthen their adaptive capacity while enhancing ecosystem and community resilience to climate change. This helped enhance water, energy and food security for local households through small-scale drinking water and irrigation systems, improved cooking stoves, agro-forestry, and integrated pest management. Local households also benefited from the reduction of climate induced disasters through the construction of dykes and check dams, and bio-engineering while also engaging in forest and biodiversity conservation through nursery establishment, plantation and livestock grazing control.</p>
