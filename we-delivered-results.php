                  <h1 class="">USD 7.5 million for Chure</h1>
                  <p>Nepal is on its way to receive additional funding from the Global Environment Facility (GEF) worth USD 7.5 million for the integrated conservation and management of the fragile Chure and associated ecosystems. The Ministry of Finance in the capacity of Operation Focal Point for GEF has committed to the GEF-6 project development which will be led by a consortium comprising the Ministry of Forests and Soil Conservation, Ministry of Agricultural Development, Ministry of Science, Technology and Environment, and WWF Nepal. The proposed GEF-6 project will focus on four focal areas in the Terai Arc Landscape: biodiversity conservation, land degradation, climate change mitigation, and sustainable forest management. In December 2013, WWF Inc. was formally accredited as the project implementing agency of GEF entrusted with the designing and implementing of GEF projects and funds. WWF Nepal became the first office in the WWF Network to pilot a three-year project under the GEF-5 replenishment from January 2014 focusing on sustainable forest management in Chure.</p>
