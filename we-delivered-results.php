          <p>Nepal’s rhino population increased by an encouraging 21% based on the 2015 rhino count data. There are now presently 645 rhinos (the highest number recorded) as compared to the 2011 estimate of 534 rhinos in Nepal’s Terai Arc Landscape. The rhino count was conducted in Chitwan National Park (605 rhinos), Parsa Wildlife Reserve (3 rhinos), Bardia National Park (29 rhinos), Shuklaphanta Wildlife Reserve (8 rhinos) and their buffer zones in the Terai Arc Landscape from 11 April-2 May. An increasing trend in rhino populations was observed in Chitwan National Park, Bardia National Park and Shuklaphanta Wildife Reserve. The count was conducted using a sweep operation where approximately 700 km2 of rhino habitat was systematically surveyed by mobilizing 54 elephants and 267 trained observers.</p>
