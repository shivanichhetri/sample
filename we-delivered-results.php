                  <h1 class="greenLight">Symposium on zero poaching</h1>
                  <p>With a view to building an Asia-wide response against poaching, a symposium, Towards Zero Poaching in Asia, was successfully organized for the first time on 2-6 February 2015 bringing together representatives from the governments of 13 tiger range countries. Hosted by the Government of Nepal’s Ministry of Forests and Soil Conservation with the technical and financial support from SAWEN, GTF, GTI, WWF’s Tigers Alive Initiative and the WWF Network, and NTNC, the symposium served to widen knowledge and learnings on different country-wide antipoaching measures while developing a Zero Poaching Toolkit to serve as a benchmark for countries to assess their own positions and statuses towards achieving zero poaching. At the symposium, Chitwan National Park also became the first site to be accredited as Conservation Assured Tiger Standard (CA|TS), demonstrating its excellence in tiger conservation and protection.</p><br>
