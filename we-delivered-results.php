                  <h1 class="">Positive sign for REDD+</h1>
                  <p>The Government of Nepal’s Ministry of Finance signed a Letter of Intent with the World Bank-Forest Carbon Partnership Facility as a next step to develop the Emissions Reduction Project Document (ERPD) in the Terai Arc Landscape following the approval of Nepal’s Emissions Reduction Project Idea Note (ER-PIN) in 2014. WWF Nepal is part of the drafting committee and is provided technical input to develop the ERPD. The ERPD will be a means to access performance-based payments under the REDD+ mechanism for the Terai Arc Landscape leveraging additional funds for local communities to support forest conservation activities while addressing the drivers of deforestation and forest degradation. The Government of Nepal’s REDD Implementation Centre will also receive an additional USD 5 million to help close the gaps identified in the REDD Readiness-package and USD 650,000 for developing the ERPD.</p>
